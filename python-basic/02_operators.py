# 연산자 우선순위: (), **, *, /, %, +, -
result = a + b * 2  
print(f"우선순위 예시 a + b * 2 = {result}")

# 우선순위 변경을 위한 괄호 사용 예시
result2 = (a + b) * 2
print(f"괄호 사용 (a + b) * 2 = {result2}")
