# 02: 산술 연산자와 문자열 연산
# 학습 목표: 기본 산술 연산자 이해 및 문자열 연산 연습, f-string 사용법 연습
# 개념:
# 1. 산술 연산자 종류 : + 덧셈, - 뺄셈, * 곱셈, / 나눗셈, % 나머지, // 몫, ** 거듭제곱
# 2. f-string이란? : 파이썬 3.6부터 도입된 문자열 포매팅 방식, 문자열 앞에 f를 붙이고, 중괄호 {} 안에 변수나 표현식을 넣으면 해당 값이 문자열 안에 삽입됨

# 숫자 변수 선언
a = 10
b = 3

# 산술 연산자 종류
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")
print(f"{a} // {b} = {a // b} (몫)")
print(f"{a} % {b} = {a % b} (나머지)")
print(f"{a} ** {b} = {a ** b} (거듭제곱)")

# 문자열 연산
str1 = "Hello"
str2 = "World"

# 문자열 덧셈(붙이기)
print(str1 + " " + str2)  
print(f"{str1} {str2}")

# 문자열 반복(곱하기)
print(str1 * 3)  
print(f"{str1 * 3}")

# 문자열와 숫자 곱하기는 가능하지만, 문자열 곱하기 실수는 불가 (아래 코드는 에러)
# print(str1 * 3.5)  # 오류 발생

# 문자열 길이 구하기
print(f"'{str1}' 문자열 길이: {len(str1)}")

# 주의: 산술 연산자는 숫자끼리만 연산 가능 (문자열 뺄셈 불가)
# 다음 줄 실행시 오류 발생(주석 처리)
